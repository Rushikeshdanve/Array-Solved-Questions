public class Main {
    public static void main(String[] args) {
        // Declare and initialize the array with a size of 5
        int[] arr = new int[5];

        // Populate the array
        for (int i = 0; i < 5; i++) {
            arr[i] = i; // Assign values to the array
        }

        // Print the elements of the array
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]); // Print each element
        }
    }
}





